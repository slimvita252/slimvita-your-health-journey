const SUPPORTED_LANGUAGES: Language[] = ["en", "es", "pt", "zh", "fr", "de"];
const STORAGE_KEY = "slimvita-language";

// Map browser language codes to our supported languages
const languageMap: Record<string, Language> = {
  // English
  "en": "en", "en-US": "en", "en-GB": "en", "en-AU": "en", "en-CA": "en",
  // Spanish
  "es": "es", "es-ES": "es", "es-MX": "es", "es-AR": "es", "es-CO": "es", "es-CL": "es",
  // Portuguese
  "pt": "pt", "pt-BR": "pt", "pt-PT": "pt",
  // Chinese
  "zh": "zh", "zh-CN": "zh", "zh-TW": "zh", "zh-HK": "zh",
  // French
  "fr": "fr", "fr-FR": "fr", "fr-CA": "fr", "fr-BE": "fr", "fr-CH": "fr",
  // German
  "de": "de", "de-DE": "de", "de-AT": "de", "de-CH": "de",
};

const detectLanguage = (): Language => {
  // Check localStorage first
  const stored = localStorage.getItem(STORAGE_KEY);
  if (stored && SUPPORTED_LANGUAGES.includes(stored as Language)) {
    return stored as Language;
  }

  // Detect from browser
  const browserLang = navigator.language || (navigator as any).userLanguage || "en";
  
  // Try exact match first
  if (languageMap[browserLang]) {
    return languageMap[browserLang];
  }
  
  // Try base language code
  const baseLang = browserLang.split("-")[0];
  if (languageMap[baseLang]) {
    return languageMap[baseLang];
  }

  // Default to English
  return "en";
};

